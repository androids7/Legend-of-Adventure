package terrain


func IntMax(a, b int) int {
    if a > b {
        return a
    } else {
        return b
    }
}
func UintMax(a, b uint) uint {
    if a > b {
        return a
    } else {
        return b
    }
}

func IntMin(a, b int) int {
    if a < b {
        return a
    } else {
        return b
    }
}
func UintMin(a, b uint) uint {
    if a < b {
        return a
    } else {
        return b
    }
}
